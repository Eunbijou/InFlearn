package pjtTest;

public class ThirteenthClass {

	public static void main(String[] args) {
		
		// 증감 연산자
		int x = 10;
		System.out.println("++x : " + (++x));
		x = 10;
		System.out.println("--x : " + (--x));
		x = 10;
		System.out.println("x++ : " + (x++));
		System.out.println("x : " + x);
		x = 10;
		System.out.println("x-- : " + (x--));
		System.out.println("x : " + x);
		
	}
}
