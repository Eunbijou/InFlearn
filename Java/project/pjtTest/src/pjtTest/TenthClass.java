package pjtTest;

public class TenthClass {

	public static void main(String[] args) {
		
		int x = 10;
		int y = 20;
		
		// 산술 연산자
		System.out.println("x + y = " + (x + y));
		System.out.println("x - y = " + (x - y));
		System.out.println("x * y = " + (x * y));
		System.out.println("x / y = " + (x / y));
		System.out.println("x % y = " + (x % y));
		
	}
}
